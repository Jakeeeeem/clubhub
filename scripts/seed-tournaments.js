#!/usr/bin/env node
/* scripts/seed-tournaments.js
   Create demo tournament event, tournament_teams, stage and simple fixtures.
   Usage: set DB env vars then: node scripts/seed-tournaments.js
*/
const { connectDB, withTransaction } = require("../backend/config/database");

async function seed() {
  await connectDB();
  return await withTransaction(async (client) => {
    // Find club and owner
    const userRes = await client.query("SELECT id FROM users LIMIT 1");
    const ownerId = userRes.rows[0]?.id;
    if (!ownerId) throw new Error("No user found; create a user first");

    const clubRes = await client.query("SELECT id FROM clubs LIMIT 1");
    const clubId = clubRes.rows[0]?.id;
    if (!clubId) throw new Error("No club found; create a club first");

    // Create tournament event
    const ev = await client.query(
      `INSERT INTO events (title, description, event_type, event_date, event_time, club_id, created_by)
       VALUES ($1,$2,$3,$4,$5,$6,$7) RETURNING id`,
      [
        "Seed Tournament Cup",
        "Demo tournament generated by seed script",
        "tournament",
        new Date().toISOString().split("T")[0],
        "10:00:00",
        clubId,
        ownerId,
      ],
    );
    const eventId = ev.rows[0].id;

    // Create 4 internal teams
    const teamIds = [];
    for (let i = 1; i <= 4; i++) {
      const t = await client.query(
        `INSERT INTO tournament_teams (event_id, team_name, status) VALUES ($1,$2,'approved') RETURNING id`,
        [eventId, `Demo Team ${i}`],
      );
      teamIds.push(t.rows[0].id);
    }

    // Create stage
    const stage = await client.query(
      `INSERT INTO tournament_stages (event_id, name, type, sequence, match_duration, break_duration) VALUES ($1,$2,$3,1,20,5) RETURNING id`,
      [eventId, "Main Stage", "knockout"],
    );
    const stageId = stage.rows[0].id;

    // Generate simple knockout fixtures (pair sequentially)
    for (let i = 0; i < teamIds.length; i += 2) {
      const home = teamIds[i];
      const away = teamIds[i + 1] || null;
      await client.query(
        `INSERT INTO tournament_matches (stage_id, event_id, home_team_id, away_team_id, round_number, match_number, status) VALUES ($1,$2,$3,$4,1,$5,'scheduled')`,
        [stageId, eventId, home, away, i / 2],
      );
    }

    // Add a couple of pitches
    await client.query(
      `INSERT INTO tournament_pitches (event_id, name, pitch_type, pitch_size) VALUES ($1,$2,$3,$4)`,
      [eventId, "Pitch 1", "Grass", "11v11"],
    );
    await client.query(
      `INSERT INTO tournament_pitches (event_id, name, pitch_type, pitch_size) VALUES ($1,$2,$3,$4)`,
      [eventId, "Pitch 2", "Artificial", "7v7"],
    );

    return { eventId, teamIds, stageId };
  });
}

seed()
  .then((r) => {
    console.log("Tournament seed complete:", r);
    process.exit(0);
  })
  .catch((err) => {
    console.error("Seed failed:", err.message || err);
    process.exit(1);
  });
